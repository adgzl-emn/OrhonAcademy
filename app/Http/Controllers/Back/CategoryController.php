     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $category = new Category;
        $category->name = $request->cname;
        $category->slug=Str::slug($request->cname);
        $category->save();
        toastSuccess('Sponsor Başarıyla olusturuldu','İşlem tamam!');
        return redirect()->route('back.category');

    }

    public function delete($id){
        $category = Category::findOrFail($id);
        $category->delete();
        toastr()->success('Yazı başarı ile silindi');
        return redirect()->back();
    }

    public function switch(Request $request){

        $blog=Category::findOrFail($request->id);
        $blog->status=$request->statu=='true' ? 1 : 0;
        $blog->save();
    }

